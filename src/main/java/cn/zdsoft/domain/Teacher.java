package cn.zdsoft.domain;

/**
 * 教师类
 * @author 段江涛
 * @date 2017-07-13
 */
public class Teacher {
	private int id;
	private String name;
	/**
	 * @return 获取 id
	 */
	public int getId() {
		return id;
	}
	/**
	 * 设置 id
	 * @param 
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return 获取 name
	 */
	public String getName() {
		return name;
	}
	/**
	 * 设置 name
	 * @param 
	 */
	public void setName(String name) {
		this.name = name;
	}
	
}
